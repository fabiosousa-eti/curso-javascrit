# Comandos javaScript

## window - representa uma janela que contém um elemento DOM

## alert - Exibe uma caixa de diálogo com uma mensagem.
> window.alert('Minha primara mensagem')

## confirm -  em JavaScript é um método do objeto window que exibe uma caixa de diálogo modal um popup com uma mensagem opcional, junto com dois botões: "OK" e "Cancelar".
> window.confirm('Esta gostando do JS')

## prompt - é uma função nativa do navegador que exibe uma caixa de diálogo com uma mensagem opcional, solicitando que o usuário insira um texto
> window.prompt('Qual é seu nome?')